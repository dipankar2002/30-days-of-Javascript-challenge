function max(n1,n2) {
  if(n1>n2) {
    return n1;
  } else {
    return n1;
  }
}
function concate(s1,s2) {
  return s1+s2;
}

console.log(`max num is ${max(2,3)}`);
console.log((`Concate string is ${concate("dip","ankar")}`));